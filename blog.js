  function contanio1(){
    var dotscon1 = document.getElementById("dotscon1");
    var conText1 = document.getElementById("contain1");
    var btncon1 = document.getElementById("btn1");
  
    if (dotscon1.style.display === "none") {
        dotscon1.style.display = "inline";
        btncon1.innerHTML = "Continue reading";
        conText1.style.display = "none";
    } 
    else {
        dotscon1.style.display = "none";
        btncon1.innerHTML = "Read less";
        conText1.style.display = "inline";
    }
  }


